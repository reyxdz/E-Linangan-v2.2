function showPopup(key) {
    const data = popupData[key];
    if (data) {
        document.getElementById('popup-title').textContent = data.title;
        document.getElementById('popup-body').innerHTML = data.content;
        
        const imageElement = document.getElementById('popup-image');
        const placeholderElement = document.getElementById('image-placeholder');
        
        if (data.image && data.image.trim() !== '') {
            // Show image
            imageElement.src = data.image;
            imageElement.style.display = 'block';
            placeholderElement.style.display = 'none';
            
            // Handle image load error
            imageElement.onerror = function() {
                imageElement.style.display = 'none';
                placeholderElement.style.display = 'flex';
            };
        } else {
            // Show placeholder
            imageElement.style.display = 'none';
            placeholderElement.style.display = 'flex';
        }
        
        document.getElementById('popup-overlay').style.display = 'block';
        document.body.style.overflow = 'hidden';
    }
}

function closePopup() {
    document.getElementById('popup-overlay').style.display = 'none';
    document.body.style.overflow = 'auto';
}

function scrollToTop() {
    window.scrollTo({
        top: 0,
        behavior: 'smooth'
    });
}

// Close popup with Escape key
document.addEventListener('keydown', function(event) {
    if (event.key === 'Escape') {
        closePopup();
    }
});

// Add scroll animations
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver(function(entries) {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.style.opacity = '1';
            entry.target.style.transform = 'translateY(0)';
        }
    });
}, observerOptions);

// Observe elements for scroll animations
document.addEventListener('DOMContentLoaded', function() {
    const animatedElements = document.querySelectorAll('.content');
    animatedElements.forEach(el => {
        el.style.opacity = '0';
        el.style.transform = 'translateY(30px)';
        el.style.transition = 'opacity 0.6s ease-out, transform 0.6s ease-out';
        observer.observe(el);
    });
});





